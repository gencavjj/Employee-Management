DROP TABLE NOTE;
DROP TABLE EMPLOYEE;

CREATE TABLE EMPLOYEE
( EMPLOYEE_ID NUMBER(10,0) NOT NULL AUTO_INCREMENT,
FIRST_NAME VARCHAR2(255) DEFAULT NULL,
LAST_NAME VARCHAR2(255) DEFAULT NULL,
EMAIL_ADDRESS VARCHAR2(255) DEFAULT NULL,
TEXT VARCHAR2(255) DEFAULT NULL,
CONSTRAINT EMPLOYEE_PK PRIMARY KEY (EMPLOYEE_ID)
);

CREATE_TABLE::= NOTE (
NOTE_ID NUMBER(10,0) NOT NULL AUTO_INCREMENT,
EMPLOYEE_ID NUMBER(10,0) NOT NULL,
TEXT VARCHAR2(255) DEFAULT NULL,
CONSTRAINT NOTE_PK PRIMARY KEY (NOTE_ID),
FOREIGN KEY (EMPLOYEE_ID) REFERENCES EMPLOYEE (EMPLOYEE_ID)
);

